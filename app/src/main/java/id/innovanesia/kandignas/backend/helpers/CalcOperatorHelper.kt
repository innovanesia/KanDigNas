package id.innovanesia.kandignas.backend.helpers

object CalcOperatorHelper
{
    fun add(leftNum: Double, rightNum: Double): Double =
        leftNum + rightNum

    fun subtract(leftNum: Double, rightNum: Double): Double =
        leftNum - rightNum

    fun divide(leftNum: Double, rightNum: Double) =
        leftNum / rightNum

    fun multiply(leftNum: Double, rightNum: Double) =
        leftNum * rightNum
}